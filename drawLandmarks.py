def read_csv(csv_path):
    data = []
    with open(csv_path, 'r') as f:
        reader = csv.reader(f)
        for row in reader:
            data.append(row)
    data = np.array(data)
    return data


def convert_landmarks(idx, llist, im):
    im_width, im_height = im.shape[1], im.shape[0]
    lms = []
    c = 0
    landmarks = llist[idx, 3:]

    for landmark in landmarks:
        landmark = float(landmark)
        if c == 0:
            # landmark_x = min(int(landmark * im_width), im_width - 1)
            landmark_x = int(im_width/2 + landmark*im_width/4)
            c = 1
        else:
            # landmark_y = min(int(landmark * im_height), im_height - 1)
            landmark_y = int(im_height/2 + landmark*im_height/4) +1
            lms.append([landmark_x, landmark_y])
            c = 0
    return lms


def pre_process_landmark(landmark_list):
    temp_landmark_list = copy.deepcopy(landmark_list)

    # Convert to relative coordinates
    base_x, base_y = 0, 0
    for index, landmark_point in enumerate(temp_landmark_list):
        if index == 0:
            base_x, base_y = landmark_point[0], landmark_point[1]

        temp_landmark_list[index][0] = temp_landmark_list[index][0] - base_x
        temp_landmark_list[index][1] = temp_landmark_list[index][1] - base_y

    # Convert to a one-dimensional list
    temp_landmark_list = list(
        itertools.chain.from_iterable(temp_landmark_list))

    # Normalization
    max_value = max(list(map(abs, temp_landmark_list)))

    def normalize_(n):
        return n / max_value

    temp_landmark_list = list(map(normalize_, temp_landmark_list))

    return temp_landmark_list


